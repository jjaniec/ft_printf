#include "../includes/ft_printf.h"

/*
** Return 0 if $c, or $c$c2 is a flag of printf, otherwise
** returns 1
*/

int		ft_is_flag_c1(char c)
{
	if (c == 's' || \
			c == 'S' || \
			c == 'p' || \
			c == 'd' || \
			c == 'D' || \
			c == 'i' || \
			c == 'o' || \
			c == 'O' || \
			c == 'u' || \
			c == 'U' || \
			c == 'x' || \
			c == 'X' || \
			c == 'c' || \
			c == 'C' || \
			c == '%' || \
			c == '#' || \
			c == '0' || \
			c == '-' || \
			c == '+' || \
			c == ' ' || \
			c == 'h' || \
			c == 'l' || \
			c == 'j' || \
			c == 'z')
		return (0);
	return (1);
}
